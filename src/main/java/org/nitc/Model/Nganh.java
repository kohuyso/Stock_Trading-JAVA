package org.nitc.Model;

public class Nganh {
    String MANGANH, TENNGANH;
    float CHISO, BIENDONG, PTBD, KHOILUONG, GIATRI, MUARONG, DONGTIEN, TYLEDT;
    
    
    
	public Nganh() {
		
	}
	public Nganh(String mANGANH, String tENNGANH, float cHISO, float bIENDONG, float pTBD, float kHOILUONG,
			float gIATRI, float mUARONG, float dONGTIEN, float tYLEDT) {

		MANGANH = mANGANH;
		TENNGANH = tENNGANH;
		CHISO = cHISO;
		BIENDONG = bIENDONG;
		PTBD = pTBD;
		KHOILUONG = kHOILUONG;
		GIATRI = gIATRI;
		MUARONG = mUARONG;
		DONGTIEN = dONGTIEN;
		TYLEDT = tYLEDT;
	}
	public String getMANGANH() {
		return MANGANH;
	}
	public void setMANGANH(String mANGANH) {
		MANGANH = mANGANH;
	}
	public String getTENNGANH() {
		return TENNGANH;
	}
	public void setTENNGANH(String tENNGANH) {
		TENNGANH = tENNGANH;
	}
	public float getCHISO() {
		return CHISO;
	}
	public void setCHISO(float cHISO) {
		CHISO = cHISO;
	}
	public float getBIENDONG() {
		return BIENDONG;
	}
	public void setBIENDONG(float bIENDONG) {
		BIENDONG = bIENDONG;
	}
	public float getPTBD() {
		return PTBD;
	}
	public void setPTBD(float pTBD) {
		PTBD = pTBD;
	}
	public float getKHOILUONG() {
		return KHOILUONG;
	}
	public void setKHOILUONG(float kHOILUONG) {
		KHOILUONG = kHOILUONG;
	}
	public float getGIATRI() {
		return GIATRI;
	}
	public void setGIATRI(float gIATRI) {
		GIATRI = gIATRI;
	}
	public float getMUARONG() {
		return MUARONG;
	}
	public void setMUARONG(float mUARONG) {
		MUARONG = mUARONG;
	}
	public float getDONGTIEN() {
		return DONGTIEN;
	}
	public void setDONGTIEN(float dONGTIEN) {
		DONGTIEN = dONGTIEN;
	}
	public float getTYLEDT() {
		return TYLEDT;
	}
	public void setTYLEDT(float tYLEDT) {
		TYLEDT = tYLEDT;
	}
    
    
}
